package com.hundsun.ares.studio.jres.database.pdm.bean;

/**
 * @author liaogc
 *
 */
public class PDMView {
	
	private String name;//视图名
	private String chineseName;//视图中文名
	private String sql;//视图对应的sql语句
	private String packageName;//包名(PDM中的包名对应工具是模块名)
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the chineseName
	 */
	public String getChineseName() {
		return chineseName;
	}
	/**
	 * @param chineseName the chineseName to set
	 */
	public void setChineseName(String chineseName) {
		this.chineseName = chineseName;
	}
	/**
	 * @return the sql
	 */
	public String getSql() {
		return sql;
	}
	/**
	 * @param sql the sql to set
	 */
	public void setSql(String sql) {
		this.sql = sql;
	}
	/**
	 * @return the packageName
	 */
	public String getPackageName() {
		return packageName;
	}
	/**
	 * @param packageName the packageName to set
	 */
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	

}
